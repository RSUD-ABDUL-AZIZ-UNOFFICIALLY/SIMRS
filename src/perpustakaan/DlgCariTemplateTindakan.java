    private void BtnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditActionPerformed
        if(TxId.getText().trim().equals("")){
            Valid.textKosong(TxTindakan,"ID");
        }else if(TxTindakan.getText().trim().equals("")){
            Valid.textKosong(TxTindakan,"Tindakan");
        }else if(TxLaporan.getText().trim().equals("")){
            Valid.textKosong(TxLaporan,"Laporan");
        }else{
            try {
                koneksi.setAutoCommit(false);
                Sequel.mengedit("template_laporan_tindakan","id='"+tbTemplate.getValueAt(tbTemplate.getSelectedRow(),0).toString()+"'","tindakan='"+TxTindakan.getText()+"',laporan='"+TxLaporan.getText()+"'");
                koneksi.setAutoCommit(true);
                if(tabMode.getRowCount()!=0){tampil();}
                emptTeks();
            } catch (SQLException ex) {
                System.out.println("Notifikasi : "+ex);
            }
        }
    }//GEN-LAST:event_BtnEditActionPerformed

