    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if(TxTindakan.getText().trim().equals("")){
            Valid.textKosong(TxTindakan,"Tindakan");
        }else if(TxLaporan.getText().trim().equals("")){
            Valid.textKosong(TxLaporan,"Laporan");
        }else{
            try {
                if (Sequel.menyimpantf("template_laporan_tindakan","'"+TxId.getText()+"','"+TxTindakan.getText()+"','"+TxLaporan.getText()+"'","Hasil Tindakan")==true){
                    tampil();
                    emptTeks();
                } else {
                    System.out.println("Notif : Gagal Simpan");
                }
            } catch (Exception ex) {
                System.out.println("Notifikasi : "+ex);
            }
        }
    }//GEN-LAST:event_BtnSimpanActionPerformed

